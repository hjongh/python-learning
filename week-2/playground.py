# Basic logical operators
print(True and False)
print(True or False)
print(not True)

# Numerical logical operators
print(5 > 0)
print(0 >= 0)
print(4 < 5)
print(4 == 3)

# Simple if statement
x = True
if x:
    print('x is True')
else:
    print('x is False')

# Multiple-branch if statement
y = 5
if y > 6:
    print('y greater than 6')
if y < 6:
    print('y less than 6')
else:
    print('y equals 6')

# For iteration loop
nums = [1, 2, 3]
for num in nums:
    print(num)

# For range loop
for i in range(3):
    print(i)

# For range loop with two parameters to range
for i in range(5, 10):
    print(i)