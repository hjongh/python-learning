# Basic logical operators
print(True and False)
print(True or False)

# Simple if statement
x = (1 == 2)
if x:
    print("1 equal to 2")
else:
    print("1 not equal to 2")

# Boolean zen
isFivePositive = (5 > 0)
if isFivePositive == True:
    print('5 > 0')
else:
    print('5 <= 0')

# For iteration loop
nums = [1, 2, 3]
for num in nums:
    print(num)

# For range loop
for i in range(3):
    print(i)